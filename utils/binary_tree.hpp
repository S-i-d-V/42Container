#ifndef BINARY_TREE_HPP
#define BINARY_TREE_HPP

/*****************************************************************************/
/*                              Namespace FT                                 */
/*****************************************************************************/

namespace ft{

	/*****************************************************************************/
	/*                                Includes                                   */
	/*****************************************************************************/

	template<class T>
	class nodeBT{

			/*****************************************************************************/
	    	/*                              Members types                                */
	    	/*****************************************************************************/

			T	_data;
			T	*_left;
			T	*_right;
			T	*_parent;
			T	*_root;

			/*****************************************************************************/
			/*                               Constructors                                */
			/*****************************************************************************/

			nodeBT();

			nodeBT(T const &src);

			/*****************************************************************************/
			/*                                Destructors                                */
			/*****************************************************************************/

			~nodeBT();

			/*****************************************************************************/
			/*                           Operators overloads                             */
			/*****************************************************************************/

			T	&operator=(T const &rhs){
				_data = rhs._data;
				_left = rhs._left;
				_right = rhs._right;
				_parent = rhs._parent;
				_root = rhs._root;
			}
	};
 
}

#endif